     * @param  \Illuminate\Http\Request  $request
     * @return Post|\Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $post = new Post();
        $post->user_id = $request->user_id;
        $post->detail = $request->detail;
        $post->save();
        return $post;
    }

